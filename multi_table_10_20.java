import java.util.*;
public class multi_table_10_20
{
    public static void main()
    {
        for(int n=10;n<=20;n++)
        { System.out.println();
            for(int i=1;i<=10;i++)
            {
                System.out.println(n+"*"+i+"="+(n*i));
            }
        }
    }
}
